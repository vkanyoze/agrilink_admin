        reader.readAsDataURL(productImage);
        
        reader.onload = function() {
            let base64Image = reader.result.split(',')[1];  // Extract base64 data
            
            let data = {
                companyid: $('#companyid').val(),
                product_name: $('#product_name').val(),
                description: $('#description').val(),
                product_weight: $('#product_weight').val(),
                product_quantity: $('#product_quantity').val(),
                product_price: $('#product_price').val(),
                product_image: base64Image
            };
            
            $.ajax({
                url: 'http://64.227.36.36:8186/agrilinkapi/admin/create_products',
                type: 'POST',
                contentType: 'application/json',
                data: JSON.stringify(data),
                headers: {
                    'Authorization': 'Bearer ' + '<?php echo $_SESSION["access_token"]; ?>'
                },
                success: function(response) {
                    alert('Product created successfully');
                    $('#productCreationModal').modal('hide');
                },
                error: function(xhr, status, error) {
                    alert('Error creating product');
                }
            });
        };
        
        reader.onerror = function(error) {
            console.log('Error: ', error);
        };
    });
});
